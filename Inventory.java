    private void addToTextAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToTextAreaActionPerformed
        // TODO add your handling code here:
        if(buyPN.getText().equals("")||buyPRC.getText().equals("")||buyQNT.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill out the form properly.", "Input Error", JOptionPane.WARNING_MESSAGE);
        } else if(Integer.parseInt(buyQNT.getText()) > qntcompare){
            JOptionPane.showMessageDialog(null, "Error! Not enough stocks for the desired quantity.", "Not enough stocks", JOptionPane.ERROR_MESSAGE);
        } else{
            //total number of items bought
            items++;
            //multiply price and quantity
            subTotal = computeSubTotal(buyQNT.getText(),buyPRC.getText());
            //computes the total cost
            totalcost += Double.parseDouble(subTotal);

            totalCost = String.valueOf(totalcost);

            //get the remaining stocks
            remainingStocks = String.valueOf(qntcompare-Integer.parseInt(buyQNT.getText()));
            
            //update the text file once the add item button is pressed
            String filePath = "C:\\Users\\Lenovo\\Desktop\\DAE_TABI_PAGPAKIARAMAN\\javaproject\\src\\javaproject\\stocks.in";
            File file = new File(filePath);
            
            ArrayList<String> tempArray = new ArrayList<>();
            
            //searches the product name from the file
            try {
                FileReader fr = new FileReader(file);
                BufferedReader br = new BufferedReader(fr);
                
                String firstLine = br.readLine().trim();
                
                String line; //for getting the input line
                String dataRow[]; //for getting the splitteed input line
                
                while((line = br.readLine()) != null){
                    dataRow = line.split(" / ");
                    if(dataRow[1].equalsIgnoreCase(buyPN.getText())){
                        tempArray.add( //adds the remaining stocks in the list with other infos retained
                            dataRow[0] + " / " +
                            dataRow[1] + " / " +
                            dataRow[2] + " / " +
                            remainingStocks
                        );
                    } else{
                        tempArray.add(line); //adds the other stocks in the array
                    }
                }
                fr.close();
                br.close();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            //updates the file
            try { 
                String firstLine = "ID , PRODUCT NAME , PRICE , QUANTITY";
                try (PrintWriter pr = new PrintWriter(file)) {
                    pr.println(firstLine);
                    
                    for(String str : tempArray){
                        pr.println(str);
                    }
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            //updates the table
            updateTheTable(jTable1, remainingStocks, prodName);
            
            //updates the fields
            prevTotal.setText("P "+totalCost);
            prev.append("(P" + buyPRC.getText() + ")\t" + buyPN.getText() + " (" + buyQNT.getText() + ")" + "\tP " + String.valueOf(subTotal) + "\n");
            buyPN.setText("");
            buyPRC.setText("");
            buyQNT.setText("");
            prevTotal.setText("Total Cost: P "+totalCost);
        }
    }//GEN-LAST:event_addToTextAreaActionPerformed

