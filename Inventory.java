    private void searchpnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchpnActionPerformed
        // TODO add your handling code here:
        String search = buyPN.getText();

        String filePath = "C:\\Users\\Lenovo\\Desktop\\DAE_TABI_PAGPAKIARAMAN\\javaproject\\src\\javaproject\\stocks.in";
        File file = new File(filePath);

        try {
            BufferedReader br = new BufferedReader(new FileReader(file));

            String firstLine = br.readLine().trim();
            Object tableLines[] = br.lines().toArray();

            for (Object tableLine : tableLines) {
                //for viewing the stocks list
                String line = tableLine.toString().trim();
                String dataRow[] = line.split(" / ");
                if(dataRow[1].equalsIgnoreCase(search)){ //if the value matches the search
                    if(dataRow[3].equalsIgnoreCase("0")){ //checks if the item is out of stock
                        JOptionPane.showMessageDialog(null, "Error! Item out of stock.", "Out of stock", JOptionPane.ERROR_MESSAGE);
                    } else{
                        buyPRC.setText(dataRow[2]);
                        qntcompare = Integer.parseInt(dataRow[3]);
                        prodName = dataRow[1];
                    }
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchpnActionPerformed

