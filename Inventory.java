    public void updateTheTable(JTable jTable1, String remainingStocks, String prodName){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        String compare;
        
        //for searching the product name in the stocks
        for(int i=0; i<jTable1.getRowCount(); i++){
            for(int j=0; j<jTable1.getColumnCount(); j++){
                compare = String.valueOf(model.getValueAt(i, j));
                
                //if the search matches
                if(compare.equalsIgnoreCase(prodName)){
                    //updates the table cell    
                    model.setValueAt(remainingStocks, i, 3);
                }
            }
        }
    }
